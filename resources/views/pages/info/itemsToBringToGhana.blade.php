        <h2>Recommended Items to Bring to Ghana</h2>
      </div>
      <div class="blog-post-img">
        <img src="https://res.cloudinary.com/hjp6lmmoa/image/upload/v1504186179/kumasi-city-ghana_kwdc80.jpg" alt="">
