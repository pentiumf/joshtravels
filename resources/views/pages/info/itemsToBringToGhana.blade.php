      <div class="blog-post-content-wrapper">

        <div class="blog-post-content">
          <p>The following list of recommended items is to assist you in your packing and preparation for your visit to Ghana.  Generally, you should pack as light as possible, given the length of your stay, in order to move around easily to and from the airport and hotels. You will likely do some shopping for yourself and friends.  Keep this in mind and bring additional travel bags that will fold down or fit within the suitcase that you are bringing to Ghana.</p>
        </div>

        <div class="blog-post-content">
          <h4>Clothing</h4>
          <p>In March, we will be at the beginning of the rainy season and the end of the dry season.  At this time of year it is unlikely that it will rain much during your visit, but it is the weather we are talking about.  It could rain a lot or very little.  Temperatures will range in the in 80’s at night and up to the 90’s in the daytime</p>
          <p>In the rainy season from May through September the temperatures during the day will drop a bit and in the evenings closer to 70’s.  The heaviest rains come in June and September.  There is a sort of “dry season” within the rainy season from mid-July to mid-August.</p>
          <p>We recommend T-Shirts and collared shirts made of “breathable” natural fibers like cotton and linen. Similarly, the same fabrics for dresses, pants/trousers and shorts</p>
          <p>Generally, men wear trousers and boys wear shorts, and women and girls wear dresses and skirts.  Women and girls dress modestly and within reason given the warm climate.  Foreigners are often excused from this general rule of dress and Western culture is changing the attitudes dress patterns of Ghanaians living here, but we are recommending that you try to stay within the established norms as much as possible.</p>
          <p>Comfortable walking closed-toe shoes for site seeing and a small hike up a hill for the visit to Kakum National Park or Wli Falls.</p>
          <p>A pair of sandals and “flip flops” for the beach.</p>
          <p>A hat and sunglasses.</p>
          <p>A swimsuit and cover up, and other beach items that you would like to have with you.</p>
          <p>There are no formal events or dinners on the schedule; therefore, you are free to pack formal clothing as you desire.</p>
        </div>

        <div class="blog-post-content">
          <h4>Medication and First Aid</h4>
          <p>Most people will not get sick nor have any medical problem.  However, it is better to have it and not need it, than need it and not have it.  Bring something for diarrhea, upset stomach, Tylenol or substitute, Sudafed or some substitute, antiseptics, bandages.  All these things are available here, but it is more convenient if you have them handy.</p>
          <p>Be sure to bring an ample supply of your prescribed medications, these items may not be available here.</p>
          <p>Malaria suppression pills (prescribed by your doctor and taken two weeks prior to your visit here).    Some of the pills that are prescribed are taken either every day or once a week depending on what you are prescribed by the doctor.  We recommend that you request something that is taken once a week for convenience.</p>
        </div>

        <div class="blog-post-content">
          <h4>Immunizations</h4>
          <p>All visitors get a varied response to travel to Ghana or West Africa from their doctors; many of them have never been here and really do not have a clue.  While they may recommend an entire series of injections, I recommend only Yellow Fever.  If you want to feel safer, you might update your tetanus shot.  That is about it.  Remember, I am not a doctor, but Yellow Fever should do it along with malaria prophylaxis medication.</p>
        </div>

        <div class="blog-post-content">
          <h4>Toiletries</h4>
          <p>Feminine napkins, toothbrush, wash cloth, wet wipes, facial tissues, insect repellent and other preferred body products.  The hotels will provide adequate towels, but you may want to bring a beach type towel.</p>
          <p>Wet wipes and gel hand cleaner come in very handy here.  There will be many occasions where you will want to wash your hands or face and no water is readily available.  The same recommendation is made with respect to travel tissue/toilet paper.</p>
          <p>I would carry a wash cloth though as most hotels don't provide them.</p>
        </div>

        <div class="blog-post-content">
          <h4>Food</h4>
          <p>You will be able to get things to munch on here since the shopping stores carry a variety of things.  However, if you feel you must bring food and may want to eat in between meals the following are not easily available here and are recommended:  Specialty crackers, specialty nuts and trail mix, raisins and other dried fruit special gums and candies that you prefer.</p>
        </div>

        <div class="blog-post-content">
          <h4>Money</h4>
          <p>Credit Cards: Visa and American Express is sometimes taken and Master Card hardly ever. Do not rely on them for payments, bring cash.</p>
          <p>Do not rely on Travelers Checks.   While they will take them here, the rate is very poor and they are a hassle to cash.  Cash is best.  $50 and $100 denominations are better than the lower ones…coins are useless here.  Ask the bank to give you the latest series and years of $50 and $100 notes.  The older versions do not have the same security provisions.  You might want to bring $1 bills for tipping.</p>
          <p>You will be able to access cash from the ATM machines here, but the fees are high.  Bring your PIN numbers.   You will not be able to access your debit account.</p>
        </div>

        <div class="blog-post-content">
          <h4>Packing</h4>
          <p>Check your airlines re: checked luggage weight and number of pieces limits as well as limits regarding carry-on for international flights. Sometimes the requirements for Africa are different than for Europe so check carefully.</p>
          <p>Even though it may be a hassle, I recommend you lock your luggage before it is checked through.  They sell special locks via TSA in the event they need to open your bags. Check TSA website to find out where you can get them.</p>
          <p>The "liquid" security provisions for carry-on luggage are still in effect.  In general I would limit my carry-on to bear essentials; something to read, change of clothing in case your luggage gets lost, medicine, extra pair of eye glasses.</p>
          <p>If you are traveling with family or close friends, I recommend you mix your clothes up amongst each other’s bags.  In the event that one bag is missing, you will still have some items in another bag.</p>
          <p>Secure all the liquids you pack by putting plastic wrap between the bottle/jar and cap then placing item in a zip-loc bag.  Do this even with newly purchased items; sometimes the caps are not even screwed on tight!</p>
        </div>

        <div class="blog-post-content">
          <h4>Other Items</h4>
          <p>The voltage here is 220 unlike the 110 in the US.  If you have electronics that require electric power, bring adapters.</p>
          <p>Bring your film or digital camera, disposable camera, Polaroid or camcorder and the film you think you will need, although there is film here. If you prefer 200 speed or better, definitely bring the film you will need during your stay.  Bring extra batteries, although you will most likely be able to find the batteries you need here.</p>
          <p>Make a copy of your passport photo page in case your passport is lost or stolen.</p>
          <p>Bring a travel guide if possible.  I recommend the West Africa Lonely Planet guide, but there are others.  If you don’t want to buy it, go to the library and borrow one for the trip.</p>
          <p>Bring a book or two to read.  A journal if you would like to keep one.</p>
          <p>Travel alarm clock, body pouch for money and passport and portable clothes iron.</p>
          <p>If you want to get your hair braided...get it done here.  It is much, much cheaper but takes time and plan appropriately.</p>
          <p>If you want to have clothes made here with African fabrics, we can assist you with this.  Attempt to purchase your fabric early in the trip to give the seamstress or tailor sufficient time to complete your order.  If you can, have your measurements prepared in advance to save time.</p>
          <p>We will provide bottled water and it is readily available here.  You will need to drink plenty to keep from getting dehydrated.</p>
          <p>Lastly, Traveling and groups generally, and moving around Ghana specifically requires a a flexible and patient disposition.</p>
