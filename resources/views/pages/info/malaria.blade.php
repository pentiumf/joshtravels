            <h4>How can malaria and other travel-related illnesses be prevented?</h4>
            <ul>
              <li>Visit your health care provider 4-6 weeks before foreign travel for any necessary vaccinations and a prescription for an antimalarial drug. </li>
              <li>Take your antimalarial drug exactly on schedule without missing doses.</li>
              <li>Prevent mosquito and other insect bites. Use DEET insect repellent on exposed skin and flying insect spray in the room where you sleep.</li>
              <li>Wear long pants and long-sleeved shirts, especially from dusk to dawn. This is the time when mosquitoes that spread malaria bite.</li>
              <li>Sleep under a mosquito bednet that has been dipped in permethrin insecticide if you are not living in screened or air-conditioned housing.</li>
