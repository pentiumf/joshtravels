            <h4>What is malaria?</h4>
            <p>Malaria is a serious, sometimes fatal, disease caused by a parasite. There are four kinds of malaria that can infect humans: Plasmodium falciparum <i>(plaz-MO-dee-um fal-SIP-a-rum), P. vivax (VI-vacks),</i> P. <i> ovale (o-VOL-ley), and P. malariae (ma-LER-ee-aa).</i></p>
          </div>
          <div class="blog-post-content">
            <h4>Where does malaria occur?</h4>
            <p>Malaria occurs in over 100 countries and territories. More than 40% of the people in the world are at risk. Large areas of Central and South America, Hispaniola (Haiti and the Dominican Republic), Africa, the Indian subcontinent, Southeast Asia, the Middle East, and Oceania are considered malaria-risk areas (an area of the world that has malaria).</p>
          </div>
          <div class="blog-post-content">
            <h4>How common is malaria?</h4>
            <p>The World Health Organization estimates that yearly 300-500 million cases of malaria occur and more than 1 million people die of malaria. About 1,200 cases of malaria are diagnosed in the United States each year. Most cases in the United States are in immigrants and travelers returning from malaria-risk areas, mostly from sub-Saharan Africa and the Indian subcontinent.</p>
          </div>
          <div class="blog-post-content">
            <h4>How do you get malaria?</h4>
            <p>Humans get malaria from the bite of a malaria-infected mosquito. When a mosquito bites an infected person, it ingests microscopic malaria parasites found in the person’s blood. The malaria parasite must grow in the mosquito for a week or more before infection can be passed to another person. If, after a week, the mosquito then bites another person, the parasites go from the mosquito’s mouth into the person’s blood. The parasites then travel to the person’s liver, enter the liver’s cells, grow and multiply. During this time when the parasites are in the liver, the person has not yet felt sick. The parasites leave the liver and enter red blood cells; this may take as little as 8 days or as many as several months. Once inside the red blood cells, the parasites grow and multiply. The red blood cells burst, freeing the parasites to attack other red blood cells. Toxins from the parasite are also released into the blood, making the person feel sick. If a mosquito bites this person while the parasites are in his or her blood, it will ingest the tiny parasites. After a week or more, the mosquito can infect another person.
            </p>
            <p>Each year in the United States, a few cases of malaria result from blood transfusions, are passed from mother to fetus during pregnancy, or are transmitted by locally infected mosquitoes.</p>
          </div>
          <div class="blog-post-content">
            <h4>What are the signs and symptoms of malaria?</h4>
            <p>Symptoms of malaria include fever and flu-like illness, including shaking chills, headache, muscle aches, and tiredness. Nausea, vomiting, and diarrhea may also occur. Malaria may cause anemia and jaundice (yellow coloring of the skin and eyes) because of the loss of red blood cells. Infection with one type of malaria, Plasmodium falciparum, if not promptly treated, may cause kidney failure, seizures, mental confusion, coma, and death.</p>
          </div>
          <div class="blog-post-content">
            <h4>How soon will a person feel sick after being bitten by an infected mosquito?</h4>
            <p>For most people, symptoms begin 10 days to 4 weeks after infection, although a person may feel ill as early as 8 days or up to 1 year later. Two kinds of malaria, P. vivax and P. ovale, can relapse; some parasites can rest in the liver for several months up to 4 years after a person is bitten by an infected mosquito . When these parasites come out of hibernation and begin invading red blood cells, the person will become sick.</p>
          </div>
